tile_rects = []

def draw_map(screen):
    global game_map
    y = 0
    for row in game_map:
        x = 0
        for tile in row:
            if tile == 1:
                screen.blit(dirt, (x * 16, y * 16))
            if tile == 2:
                screen.blit(grass, (x * 16, y * 16))
            if tile != 0:
                tile_rects.append(pygame.Rect(x * 16, y * 16, 16, 16))
            x += 1
        y += 1